						<h2>&ldquo; What we are &rdquo;</h2>
						<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;苏州大学计算机爱好者协会（Computerssociation of Amateur。简称CAA）是由校党委领导，受学院团委监督，挂靠于计算机科学与技术学院社团联合会的学术科技类社团。成立于2004年，协会会员近百人，规模属于学院第一大社团。<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
						协会宗旨是积极开展计算机学术活动和技术交流，大力推广和普及计算机科学技术知识，调动学生积极性和主动性，激发兴趣，培养探索创性能力和组织管理能力。
						计算机爱好者协会曾与多家公司进行合作：与追梦网进行合作组建派星人俱乐部；与蚂蚁网合作建立苏州本地实践基地；与百度合作成立百度测试学院；今年，我们与微软合作成立了苏州大学微软学生俱乐部。一步步走来，我们在探索，也在不断成长和收获。</p><p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
						活动方面，去年，我们举办了“寻梦东吴，缘起姑苏”的创意征集活动有心追梦的人提供了寻找伙伴，实现梦想的舞台。今年在俱乐部的努力下，与上交、复旦、同济、华师大成功举办首届华东区微软Hackathon比赛。苏州大学参赛队伍首次亮相此次大赛就做出了杰出的成果，展示了苏大的风采！为苏州大学参加微软菁英计划提供了优秀的项目资源。同时，在校内，我们与ACM集训队合作举办一系列编程比赛，为学校选拔更多的技术人才。社团内部，我们也开展了技术沙龙活动，进一步提升社团成员的技术实力，为以后的项目开发奠定基础。</p>
